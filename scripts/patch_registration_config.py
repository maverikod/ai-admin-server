"""Merge mTLS proxy registration settings into config/config.json.

Certificate paths point at the code_analysis repo ``mtls_certificates/`` tree
(relative to repo root). Override hosts via environment:

- VAST_SRV_PROXY_HOST (default 172.28.0.2) - MCP proxy on Docker network
- VAST_SRV_ADVERTISED_HOST (default 172.28.0.1) - host the proxy can reach for
  pre-registration health check; set to ``vast_srv`` in compose when appropriate
- VAST_SRV_BIND_HOST (default 0.0.0.0)
- VAST_SRV_PORT (default 8060)
"""

from __future__ import annotations


import json

import os

import uuid

from pathlib import Path


PROJECT_ROOT = Path(__file__).resolve().parent.parent

CONFIG_PATH = PROJECT_ROOT / "config" / "config.json"

REPO_ROOT = PROJECT_ROOT.parent.parent

MTLS_PREFIX = Path("mtls_certificates") / "mtls_certificates"


def _abs(*parts: str) -> str:
    return str((REPO_ROOT / MTLS_PREFIX / Path(*parts)).resolve())
def main() -> None:
    """Merge registration URLs and certificates into config.json."""
    proxy_host = os.environ.get("VAST_SRV_PROXY_HOST", "172.28.0.2")
    advertised = os.environ.get("VAST_SRV_ADVERTISED_HOST", "172.28.0.1")
    bind_host = os.environ.get("VAST_SRV_BIND_HOST", "0.0.0.0")
    server_port = int(os.environ.get("VAST_SRV_PORT", "8060"))

    raw = CONFIG_PATH.read_text(encoding="utf-8")
    data = json.loads(raw)
    # SimpleConfig ServerConfig rejects engine-only keys (e.g. reload/workers).
    _srv = data.get("server")
    if isinstance(_srv, dict):
        _srv.pop("reload", None)
        _srv.pop("workers", None)

    server = data.setdefault("server", {})
    server["host"] = bind_host
    server["port"] = server_port
    server["protocol"] = "mtls"
    server["advertised_host"] = advertised
    server.setdefault("servername", "vast_srv")
    ssl_srv = server.setdefault("ssl", {})
    ssl_srv["cert"] = _abs("server", "code-analysis-server.crt")
    ssl_srv["key"] = _abs("server", "code-analysis-server.key")
    ssl_srv["ca"] = _abs("ca", "ca.crt")
    ssl_srv["crl"] = None
    ssl_srv["dnscheck"] = False
    ssl_srv["check_hostname"] = False

    prev_reg = data.get("registration")
    prev_uuid = None
    if isinstance(prev_reg, dict):
        prev_uuid = prev_reg.get("instance_uuid")
    reg = {}
    data["registration"] = reg
    reg["enabled"] = True
    reg["protocol"] = "mtls"
    reg["register_url"] = f"https://{proxy_host}:3004/register"
    reg["unregister_url"] = f"https://{proxy_host}:3004/unregister"
    reg["heartbeat_interval"] = 30
    reg["server_id"] = os.environ.get("VAST_SRV_SERVER_ID", "vast_srv")
    reg["server_name"] = os.environ.get("VAST_SRV_SERVER_NAME", "vast_srv")
    reg["instance_uuid"] = prev_uuid or str(uuid.uuid4())
    reg["auto_on_startup"] = True
    reg["auto_on_shutdown"] = True
    ssl_reg = reg.setdefault("ssl", {})
    ssl_reg["cert"] = _abs("client", "code-analysis.crt")
    ssl_reg["key"] = _abs("client", "code-analysis.key")
    ssl_reg["ca"] = _abs("ca", "ca.crt")
    ssl_reg["crl"] = None
    ssl_reg["dnscheck"] = False
    ssl_reg["check_hostname"] = False
    hb = reg.setdefault("heartbeat", {})
    hb["url"] = f"https://{proxy_host}:3004/proxy/heartbeat"
    hb["interval"] = 30

    CONFIG_PATH.write_text(
        json.dumps(data, indent=2, ensure_ascii=False) + "\n",
        encoding="utf-8",
    )
    print("ok", CONFIG_PATH)


if __name__ == "__main__":
    main()


ROOT = Path(__file__).resolve().parent.parent


CFG = ROOT / "config" / "config.json"


async def run() -> int:
    """Register with proxy; bounded by asyncio.wait_for for MCP tooling."""
    logging.basicConfig(level=logging.INFO)
    cfg = get_config()
    cfg.config_path = str(CFG)
    cfg.load_config()
    data = cfg.get_all()
    reg = data.get("registration") or {}
    if not reg.get("enabled"):
        print("registration.enabled is false; abort")
        return 2
    mgr = RegistrationManager()
    try:
        ok = await asyncio.wait_for(
            mgr.register_with_proxy(data),
            timeout=28.0,
        )
    except asyncio.TimeoutError:
        print(
            "register_with_proxy exceeded deadline (asyncio); check proxy and",
            "MCP_PROXY_ADAPTER_HTTP_TIMEOUT",
        )
        return 1
    print("register_with_proxy returned:", ok)
    return 0 if ok else 1


def main() -> None:
    """Run the async probe under asyncio.run."""
    raise SystemExit(asyncio.run(run()))



"""Start vast_srv briefly, then run live registration against the proxy (bounded)."""

from __future__ import annotations


import asyncio


import importlib.util


import logging


import socket


import subprocess


import sys


import time


from pathlib import Path


ROOT = Path(__file__).resolve().parent.parent


CFG = ROOT / "config" / "config.json"


PROBE_PATH = ROOT / "scripts" / "live_registration_probe.py"


SERVER_MODULE = "ai_admin.server"

STARTUP_WAIT_SEC = 90.0


POLL_INTERVAL_SEC = 0.25


def _wait_tcp(host: str, port: int, deadline: float) -> bool:
    """Poll TCP until connect succeeds or deadline elapses."""
    end = time.monotonic() + deadline
    while time.monotonic() < end:
        try:
            with socket.create_connection((host, port), timeout=1.0):
                return True
        except OSError:
            time.sleep(POLL_INTERVAL_SEC)
    return False


async def _run_registration() -> int:
    """Load live probe by path and await its run()."""
    spec = importlib.util.spec_from_file_location("_lrp", PROBE_PATH)
    if spec is None or spec.loader is None:
        print("cannot load live_registration_probe from", PROBE_PATH)
        return 3
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    run_fn = getattr(mod, "run")
    return int(await run_fn())


def main() -> None:
    """Spawn AI Admin server, run registration probe, stop server."""
    logging.basicConfig(level=logging.INFO)
    host = "127.0.0.1"
    port = 8060
    cmd = [sys.executable, "-m", SERVER_MODULE, "--config", str(CFG)]
    log_path = ROOT / "logs" / "bounded_autoreg_ai_admin.log"
    log_path.parent.mkdir(parents=True, exist_ok=True)
    log_f = log_path.open("w", encoding="utf-8")
    proc = None
    try:
        # File-backed stdout/stderr: no PIPE deadlock; tail printed on failure.
        proc = subprocess.Popen(
            cmd,
            cwd=str(ROOT),
            stdin=subprocess.DEVNULL,
            stdout=log_f,
            stderr=subprocess.STDOUT,
        )
        if not _wait_tcp(host, port, STARTUP_WAIT_SEC):
            try:
                log_f.flush()
            except OSError:
                pass
            try:
                log_f.close()
            except OSError:
                pass
            tail = ""
            try:
                txt = log_path.read_text(encoding="utf-8", errors="replace")
                lines = txt.splitlines()
                tail = "\n".join(lines[-120:]) if len(lines) > 120 else txt
            except OSError as exc:
                tail = f"<could not read log: {exc}>"
            print(
                "server did not listen on",
                host,
                port,
                "within",
                STARTUP_WAIT_SEC,
                "s (exit code",
                proc.poll() if proc else None,
                ")",
            )
            print("--- server log tail ---")
            print(tail)
            raise SystemExit(3)
        rc = asyncio.run(_run_registration())
        raise SystemExit(rc)
    finally:
        if proc is not None:
            proc.terminate()
            try:
                proc.wait(timeout=10)
            except subprocess.TimeoutExpired:
                proc.kill()
        try:
            log_f.close()
        except OSError:
            pass


if __name__ == "__main__":
    main()

